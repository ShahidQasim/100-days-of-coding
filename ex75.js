// Compound Assignment Operators: Use compound assignment operators to simplify arithmetic operations. 
//Start with `x = 4` and perform a series of operations (addition, subtraction, multiplication, 
//division) on `x` using compound operators.
function compoundoperators() {
    let x = 4;
    console.log("initial value of x", 4);
    x += 2;
    console.log("adding 2 to x", x);
    x -= 2;
    console.log("subtractinf 2 from x", x);
    x *= 2;
    console.log("multiply x by 2", x);
    x /= 8;
    console.log("dividing x by 8", x);
}
compoundoperators();
